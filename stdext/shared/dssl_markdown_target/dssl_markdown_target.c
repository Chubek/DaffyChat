DSSLTarget* dssl_plugin_create(void) {
    DSSLTarget* target = (DSSLTarget*)calloc(1, sizeof(DSSLTarget));
    if (target == NULL) {
        return NULL;
    }

    target->name = "markdown_index";
    target->init = markdown_init;
    target->finalize = markdown_finalize;
    target->emit_struct = markdown_emit_struct;
    target->emit_enum = markdown_emit_enum;
    target->emit_union = markdown_emit_union;
    target->emit_rpc = markdown_emit_rpc;
    target->emit_rest = markdown_emit_rest;
    target->emit_exec = markdown_emit_exec;
    return target;
}

void dssl_plugin_destroy(DSSLTarget* target) {
    if (target == NULL) {
        return;
    }

    /*
     * Defensive cleanup: if the host forgot to finalize, do it here too.
     * This keeps the sample simple and leak-resistant.
     */
    if (target->priv != NULL) {
        markdown_finalize(target);
    }
    free(target);
}
